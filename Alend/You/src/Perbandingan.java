public class Perbandingan {
    public static void main(String [] args) {
        int value1 = 100, value2 = 100;
        System.out.println(value1>value2);
        System.out.println(value1<value2);
        System.out.println(value1>=value2);
        System.out.println(value1<=value2);
        System.out.println(value1==value2);
        System.out.println(value1!=value2);
    }
}
